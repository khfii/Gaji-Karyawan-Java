      public void tampil(){
      DefaultTableModel tbl = new DefaultTableModel(); 
      tbl.addColumn("ID Karyawan");
      tbl.addColumn("Nama");
      tbl.addColumn("Jabatan");
      tbl.addColumn("Jam Kerja");
      tbl.addColumn("Jam lembur");
      tbl.addColumn("Gaji Bersih");
      
      
      try {
          Connection c = koneksi.getKoneksi(); 
          Statement s = c.createStatement(); 
          String sql = "select * from gajipegawai"; 
          ResultSet r = s.executeQuery(sql);  
          while(r.next()){
              tbl.addRow(new Object[]{
                  r.getString("id"),
                  r.getString("nama"), 
                  r.getString("jabatan"), 
                  r.getString("jam_kerja"), 
                  r.getString("jam_lembur"), 
                  r.getString("gaji")
                 
                
                  
              });
             tablePegawai.setModel(tbl);
            
          }
      } catch (Exception e) {
      }
      
  }
    
      
    

