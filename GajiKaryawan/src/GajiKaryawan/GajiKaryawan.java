    private void tablePegawaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablePegawaiMouseClicked
       int row = tablePegawai.getSelectedRow(); 
       if(row==row){
           fieldId.setEnabled(false);
           
       }else{
           fieldId.setEnabled(true);
       }
       String tabel_klik = (tablePegawai.getModel().getValueAt(row, 0).toString()); 
        try {
       java.sql.Connection conn = (java.sql.Connection)koneksi.getKoneksi(); 
       java.sql.Statement stm = conn.createStatement(); 
       java.sql.ResultSet sql = stm.executeQuery
        ("select * from gajipegawai where id = '"+tabel_klik + "'");
       if(sql.next()){
           String add1 = sql.getString("id"); 
           fieldId.setText(add1);
           
           
           String add2 = sql.getString("nama"); 
           fieldNama.setText(add2);
           
           String add3 = sql.getString("jabatan"); 
           comboJabatan.setSelectedItem(add3);
           
           String add4 = sql.getString("jam_kerja"); 
           fieldJamKerja.setText(add4);
           
           String add5 = sql.getString("jam_lembur"); 
           fieldJamLembur.setText(add5);
           
           String add6 = sql.getString("gaji"); 
           fieldGajibersih.setText(add6);
           
        
           
       
       }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_tablePegawaiMouseClicked

