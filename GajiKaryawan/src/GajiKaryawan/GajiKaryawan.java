    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
       id = Integer.valueOf(fieldId.getText()); 
       nama = fieldNama.getText(); 
       jabatan = (String) comboJabatan.getSelectedItem(); 
       idJabatan = comboJabatan.getSelectedIndex(); 
       jamKerja = Integer.valueOf(fieldJamKerja.getText()); 
       jamLembur = Integer.valueOf(fieldJamLembur.getText()); 
       
       if(idJabatan ==0){
           gajiBersih = (jamKerja * 50000) + (jamLembur * 40000) + (200000); 
       }
       else if(idJabatan==1){
           gajiBersih = (jamKerja * 40000) + (jamLembur * 30000) + (150000); 
       }else if(idJabatan==2){
           gajiBersih = (jamKerja * 30000) + (jamLembur * 20000) + (100000); 
           
       }else if(idJabatan==3){
             gajiBersih = (jamKerja * 20000) + (jamLembur * 10000) + (50000); 
       }else {
           gajiBersih = 0; 
       }
       
       fieldGajibersih.setText(String.valueOf("Rp. " + gajiBersih)); 
      
      
       
    }//GEN-LAST:event_jButton6ActionPerformed

